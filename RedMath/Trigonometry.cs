using System;

namespace RedMath
{
    public static class Trigonometry
    {
        public static double Sin(double x)
        {
            return Math.Sin(x);
        }

        public static double Cos(double x)
        {
            return Math.Cos(x);
        }

        public static double Arctan2(double x, double y)
        {
            return Math.Atan2(y, x);
        }
    }
}
